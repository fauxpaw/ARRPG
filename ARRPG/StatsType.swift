import Foundation

enum StatsType {
    
    case LVL // Level
    case EXP // Experience
    case HP  // Hit Points
    case MHP // Max Hit Points
    case MP  // Magic Points
    case MMP // Max Magic Points
    case ATK // Base Physical Attack
    case DEF // Base Physical Defense
    case MAT // Base Arcane Attack
    case MDF // Base Arcane Defense
    case EVD // Evade
    case RES // Status Resistance
    case SPD // Speed
    case PAT // Piercing Attack
    case CAT // Crushing Attack
    case SAT // Slashing Attack
    case PDF // Piercing Defense
    case CDF // Crushing Defense
    case SDF // Slashing Defense
    case FAT // Fire Attack
    case WAT // Water Attack
    case AAT // Air Attack
    case EAT // Earth Attack
    case FR  // Fire Resistance
    case AR  // Air Resistance
    case WR  // Water Resistance
    case ER  // Earth Resistance
    
}
